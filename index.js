addEventListener('fetch', event => {
  event.respondWith(
    handleRequest(event.request).catch(
      err => new Response(err.stack, { status: 500 }),
    ),
  );
});

async function handleRequest(request) {
  const { origin, pathname } = new URL(request.url);

  if (pathname == '/' || pathname == '/txt/' || pathname == '/txt/index.html') {
    return Response.redirect(origin + '/txt/hutrua.txt', 301);
  }

  if (pathname.startsWith('/txt/') && pathname.endsWith('.txt')) {
    let name = pathname.substr(5);
    return handleTxt(name);
  }

  return handleTxt('404.txt');
}

function escapeHtml(unsafe) {
  return unsafe
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}


function html_template(content) {
  let txt = content.txt.split('\n');
  for (let i = 0; i < txt.length; i++) {
    txt[i] = '<p>' + escapeHtml(txt[i]) + '</p>';
  }
  txt = txt.join('\n');
  return `<!DOCTYPE html>
<html lang="zh-CN">
<head>
  <meta charset="utf-8">
  <meta http-equiv="X-UA-Compatible" content="IE=edge">
  <meta name="viewport" content="width=device-width, initial-scale=1">
  <title>${content.name}</title>
    <link rel="icon" type="image/png" href="/assets/images/favicon.png">
    <link rel="icon" type="image/svg+xml" href="/assets/images/favicon.svg">
    <link rel="apple-touch-icon" sizes="180x180" href="/assets/images/apple-touch-icon.png">
</head>
<body>
  <main>
    <h1>${content.name}</h1>
    ${txt}
  </main>
  <footer>
    <p style='color:grey;font-size:0.7em;'>This page is generated by Cloudflare Workers.</p>
  </footer>
</body>
</html>`;
}

async function handleTxt(name) {
  let txt = await TXT.get(name);
  if (txt == null) {
    txt = '404 not found';
  }
  let content = { name: name, txt: txt};
  return new Response(html_template(content), {
    headers: { 'content-type': 'text/html;charset=UTF-8' },
  });
}
